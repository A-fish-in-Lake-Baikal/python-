# 以论语中一句话作为字符串变量 s，补充程序，分别输出字符串 s 中汉字和标点符号的个数。
s = "学而时习之,不亦说乎?有朋自远方来,不亦乐乎?人不知而不愠,不亦君子乎?"
m = 0  # 汉字个数
n = 0  # 符号字数
for l in s:
    if '\u4e00' <= l <= '\u9fff':
        m += 1
    else:
        n += 1

print("汉字个数：{}，符号个数：{}".format(m, n))
